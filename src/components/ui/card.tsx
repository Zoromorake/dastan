import * as React from "react"

import { cn } from "@/lib/utils"

function Card({
  className,
  size = "default",
  ...props
}: React.ComponentProps<"div"> & { size?: "default" | "sm" }) {
  return (
    <div
      data-slot="card"
      data-size={size}
      className={cn(
        "flex flex-col overflow-hidden rounded-xl border border-stone-200 bg-white text-sm text-stone-900 shadow-sm",
        className
      )}
      {...props}
    />
  )
}

